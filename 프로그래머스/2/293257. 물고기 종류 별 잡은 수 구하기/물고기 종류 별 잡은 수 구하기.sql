WITH FISH_CAUGHT AS (
    SELECT
        COUNT(ID) AS FISH_COUNT,
        FISH_TYPE
    FROM
        FISH_INFO AS FI
    GROUP BY
        FISH_TYPE
)

SELECT
    FISH_COUNT,
    FISH_NAME
FROM 
    FISH_CAUGHT
    LEFT JOIN
    FISH_NAME_INFO
    USING(FISH_TYPE)
ORDER BY
    FISH_COUNT DESC
